                                                    <h3>Exercise Regularly </h3>
                                                    <p>For a lot of people, exercising regularly seems like an impossible task because of the hardwork and discipline it entails. However, there are a few tips one can follow to maintain a consistent exercising regimen. Firstly is to set a goal for what you want to achieve with exercising e.g. lose xx kg in a year, go from size 14 to 10, etc. Then break down this goal into small achievable steps,e.g. Run 200m every day, Take 10000 steps a day, Run on the treadmill for 20 minutes every day, Lift 500kg weights every day. <br><br>You can start with simple exercises like taking regular walks daily. Then use an exercise app like Pedometer Step Counter or a Fitbit to set goals for the number of steps you want to take per day, and how track the number of steps you take per day. Achieving your set walking goals everyday will spur you to continue; and with time, you can increase your step limit for each day. You can also ask a friend to join you in this journey so you become an accountability partner for each other, which will make it more fun for you.
                                                    </p>
                                                    <p></p>
                                                    <p></p>

                                                    <h3>Go for Medical Check Ups </h3>
                                                    <p>Medical check ups is mostly viewed as something for the aged, while young and active people only go hospitals when they are sick. However, with the increasing rate of sudden dates, regular medical check ups is now more important than ever. Going for check ups regularly can help detect something wrong long before it becomes serious and incapacitates a person. General body check ups are recommended at least twice a year, while cancer screenings are recommended once a year or if any tell-tale signs of cancer is observed. Also, vaccinations against diseases like tetanus, hepatitis, etc. should be taken as at when due. Most health insurance packages cover check ups, medical screenings and vaccinations, so take advantage of them to ensure you remain healthy.
                                                    </p>
                                                    <p></p>
                                                    <p></p>



                                                    <h3>Rest</h3>
                                                    <p>The health benefits of resting cannot be overemphasized. As you try to achieve all your other goals, make out some time to rest. Resting relaxes your body, helps you become more productive, makes the immune system stronger and reduces risk of heart disease. At least seven hours of sleep daily is recommended for adults from 18 years and above. However, kids, pregnant women and the aged need more hours of sleep daily. As Tom Roth will say, Sleep is an investment in the energy you need to be effective tomorrow.
                                                    </p>
                                                    <p></p>
                                                    <p></p>


                                                    <h3>Stay Positive </h3>
                                                    <p>As you care for your physical body, you also have to take care of your mental health. Depression has become one of the leading causes of suicide among young people especially. While here are some situations that can lead one to fall apart and give up, it is important to always rise above set backs and keep surging forward. One of the ways to do this is stay positive all the time and adopt a growth mindset. This means never letting negative thoughts take root in your mind, learning from set backs, implementing lessons learned and trying again and again. It will not always be easy to do this, but staying and remaining down is never an option. Always remember that it is not failure if you learn from it and apply the lessons going forward.
                                                    </p>
