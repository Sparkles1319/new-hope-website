                                                    <p></p>
                                                    <h3>Eat (and Drink) Right</h3>
                                                    <p>A healthy body starts from what we take in. According to the World Health Organization (WHO), a healthy diet helps to protect against malnutrition in all its forms, as well as non-communicable diseases (NCDs), including such as diabetes, heart disease, stroke and cancer. A healthy diet should include fruit, vegetables, legumes (e.g. lentils and beans), nuts and whole grains (e.g. unprocessed maize, millet, oats, wheat and brown rice). Also, limit intake of free sugars to less than 10% of total energy intake and reduce salt intake to less than 5g a day (which is less than a tea spoon). Remember, water is the healthiest drink, and your body needs a lot of it to function and be in good shape. </p>
